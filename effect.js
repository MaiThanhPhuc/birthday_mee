$(window).load(function () {
  $(".loading").fadeOut("fast");
  $(".container").fadeIn("fast");
});
$("document").ready(function () {
  var vw;
  $(window).resize(function () {
    vw = $(window).width() / 2;
  });

  $("#turn_on").click(function () {
    var audio = $(".song")[0];
    audio.play();
    $("body").addClass("peach");
    $(".bannar").addClass("bannar-come");
    $(".cake").fadeIn("slow");
    $(".balloons").addClass("hotairballoon");
    $(this)
      .fadeOut("slow")
      .delay(4000)
      .promise()
      .done(function () {
        $("#light_candle").fadeIn("slow");
      });
  });

  $("#light_candle").click(function () {
    $(".fuego").fadeIn("slow");
    $(this)
      .fadeOut("slow")
      .delay(3000)
      .promise()
      .done(function () {
        $("#story").fadeIn("slow");
      });
  });

  $("#story").click(function () {
    $(this).fadeOut("fast");
    $(".cake")
      .fadeOut("fast")
      .promise()
      .done(function () {
        animationTimeline();
      });
  });
});

